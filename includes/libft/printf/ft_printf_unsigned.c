#include "../libft.h"

void	ft_printf_unsigned(unsigned int nb, int *ptr)
{
	if (nb == 4294967295)
		ft_printf_str("4294967295", ptr);
	else
		ft_printf_nbr(nb, "0123456789", ptr);
}
