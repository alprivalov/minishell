#include "../libft.h"

int	ft_lstsize(t_list *lst)
{
	size_t	nb;

	nb = 0;
	if (lst == NULL)
		return (0);
	while (lst != NULL)
	{
		lst = lst->next;
		nb++;
	}
	return (nb);
}
